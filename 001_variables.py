# Variables

"""
Es un nombre que se refiere a un objeto que reside en la memoria. 
El objeto puede ser de alguno de los tipos vistos (número o cadena de texto), 
o alguno de los otros tipos existentes en Python.

Cada variable debe tener un nombre único llamado identificador.
Eso es muy de ayuda pensar las variables como contenedores que contienen 
data el cual puede ser cambiado después a través de técnicas de programación.
"""
c = "Hola Mundo" # cadenas de caracteres
print(type(c)) # comprobar tipo de dato

e = 23 # número entero
print(type(e)) # comprobar tipo de dato
